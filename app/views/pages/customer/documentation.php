<h3>Tentang Rezerva</h3>
<p>Rezerva merupakan sebuah sistem informasi reservasi restoran yang berfungsi untuk memanajemen pengelolaan restoran Dengan berbagai fitur.Pembuatan Rezerva dilatarbelakangi oleh keinginan dari tim rezerva untuk membantu berbagai restoran di indonesia dalam mengelola proses pemesanan,pembayaran hingga pengelolaan restoran itu sendiri seperti pengelolaan keuangan,pendapatan, makanan dan sejenisnya.Target aplikasi Rezerva adalah untuk restoran restoran yang kesulitan dalam mengelola restorannya dikarenakan semakin berkembangnya restoran yang menyebabkan semakin banyaknya pelanggan dan karyawan sehingga menyebabkan kesulitan dalam pengelolaanya. Rezerva sendiri terdiri dari dua subsistem yaitu sistem untuk Customer dan sistem untuk admin. Untuk Customer terdapat Fitur Seperti memilih Pesanan/menu, pembayaran pesanan, memberikan rating, dan Keranjang. Disisi lain Admin memiliki Fitur yang cukup superior yaitu Dashboard,Pengelolaan daftar pesanan,Laporan Pendapatan,Laporan Menu,Kelola Menu,Kelola Kategori , dan Kelola Admin. Dengan Adanya aplikasi Rezerva ini diharapkan dapat memudahkan sistem pengelolaan pada restoran sehingga meningkatkan kemungkinan pendapatan dan produktivitas pada restoran yang menggunakan aplikasi ini.</p><br>

